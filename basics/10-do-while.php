<?php

	//Do-While loops
	
	$iNum = 1;

	//Do output $iNum and do a +1 each time it loops. Stop the loop when it reaches 10 ('While ($iNum <= 10)').
	// The advantage is that the 'Do' is always executed until it reaches the given code in While.
	do {
		echo $iNum . '<br>';
		$iNum++;
	} while ( $iNum <= 10);



?>
